function normalizePlayerKey(name: string) {
  return String(name || "")
    .toLowerCase()
    .trim()
    .normalize("NFD")
    .replace(/\p{Diacritic}/gu, "")
    .replace(/[^a-z0-9]/g, "");
}

const PLAYER_PHOTO_BY_NORMALIZED: Record<string, string> = Object.keys(PLAYER_PHOTO_OVERRIDES).reduce(
  (acc, k) => {
    acc[normalizePlayerKey(k)] = PLAYER_PHOTO_OVERRIDES[k];
    return acc;
  },
  {} as Record<string, string>
);

export function getPlayerPhotoOverride(playerName: string | null | undefined) {
  if (!playerName) return undefined;
  const trimmed = String(playerName).trim();
  if (PLAYER_PHOTO_OVERRIDES[trimmed]) return PLAYER_PHOTO_OVERRIDES[trimmed];
  const normalized = normalizePlayerKey(trimmed);
  return PLAYER_PHOTO_BY_NORMALIZED[normalized];
}

function normalizeTeamKey(name: string) {
  let v = String(name || "").toLowerCase().trim();
  v = v
    .normalize("NFD")
    .replace(/\p{Diacritic}/gu, "")
    .replace(/[^a-z0-9]/g, "");
  if (v.endsWith("afc")) v = v.slice(0, -3);
  if (v.endsWith("fc")) v = v.slice(0, -2);
  if (v.endsWith("cf")) v = v.slice(0, -2);
  return v;
}

